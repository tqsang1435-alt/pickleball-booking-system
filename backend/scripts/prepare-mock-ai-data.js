const sql = require("mssql");
const path = require("path");
require("dotenv").config({ path: path.join(__dirname, "../.env") });

async function run() {
  console.log("Connecting to Database server for mock data insertion:", process.env.DB_SERVER || "localhost");
  const pool = await sql.connect({
    user: process.env.DB_USER,
    password: process.env.DB_PASSWORD,
    database: process.env.DB_DATABASE,
    server: process.env.DB_SERVER || "localhost",
    options: { encrypt: false, trustServerCertificate: true },
  });

  console.log("Connected. Clearing old AI mock bookings...");
  
  // Clear any existing mock data generated by this script
  await pool.query(`
    DELETE FROM BookingDetails WHERE BookingID IN (SELECT BookingID FROM Bookings WHERE BookingCode LIKE 'AI_MOCK_%');
    DELETE FROM Payments WHERE BookingID IN (SELECT BookingID FROM Bookings WHERE BookingCode LIKE 'AI_MOCK_%');
    DELETE FROM Bookings WHERE BookingCode LIKE 'AI_MOCK_%';
  `);

  console.log("Generating 10 days of historical bookings (dates T-10 to T-1)...");
  
  const today = new Date();
  
  // Let's insert bookings for 10 distinct days
  for (let d = 1; d <= 10; d++) {
    const date = new Date(today);
    date.setDate(today.getDate() - d);
    const dateStr = date.toISOString().split("T")[0];
    const isWeekend = date.getDay() === 0 || date.getDay() === 6;
    
    console.log(`Inserting bookings for ${dateStr} (Weekend: ${isWeekend})`);
    
    // We will generate slots for Court 1, 2, 3, 4
    for (let courtId = 1; courtId <= 4; courtId++) {
      // Pick active hours: mornings (7-10) and evenings (17-21) are busier.
      // Weekend has more afternoon bookings.
      const hours = [7, 8, 9, 10, 15, 17, 18, 19, 20, 21];
      
      for (const hr of hours) {
        // Probabilities: Weekend mornings & evenings = 80%, weekdays = 40%
        let prob = isWeekend ? 0.8 : 0.4;
        
        // Add random variation
        if (Math.random() < prob) {
          const bookingCode = `AI_MOCK_${dateStr.replace(/-/g, "")}_C${courtId}_H${hr}`;
          
          // UserID 1 is usually seed Admin or default user
          const bookingResult = await pool.request()
            .input("BookingCode", sql.NVarChar(50), bookingCode)
            .input("BookingDate", sql.Date, dateStr)
            .query(`
              INSERT INTO Bookings (BookingCode, UserID, BookingType, BookingDate, CourtFee, TotalAmount, Status, CreatedAt)
              OUTPUT INSERTED.BookingID
              VALUES (@BookingCode, 1, 'Court', @BookingDate, 150000.0, 150000.0, 'Completed', DATEADD(hour, -2, GETDATE()))
            `);
            
          const bookingId = bookingResult.recordset[0].BookingID;
          const startTimeStr = `${String(hr).padStart(2, "0")}:00:00`;
          const endTimeStr = `${String(hr + 1).padStart(2, "0")}:00:00`;
          
          await pool.request()
            .input("BookingID", sql.Int, bookingId)
            .input("CourtID", sql.Int, courtId)
            .input("BookingDate", sql.Date, dateStr)
            .input("StartTime", sql.VarChar(8), startTimeStr)
            .input("EndTime", sql.VarChar(8), endTimeStr)
            .query(`
              INSERT INTO BookingDetails (BookingID, CourtID, BookingDate, StartTime, EndTime, CourtFee, CoachFee, SubTotal)
              VALUES (@BookingID, @CourtID, @BookingDate, @StartTime, @EndTime, 150000.0, 0, 150000.0)
            `);
            
          const paymentCode = `PAY_MOCK_${dateStr.replace(/-/g, "")}_C${courtId}_H${hr}`;
          const transactionCode = `TX_MOCK_${dateStr.replace(/-/g, "")}_C${courtId}_H${hr}`;

          await pool.request()
            .input("BookingID", sql.Int, bookingId)
            .input("PaymentCode", sql.NVarChar(50), paymentCode)
            .input("TransactionCode", sql.NVarChar(100), transactionCode)
            .query(`
              INSERT INTO Payments (BookingID, PaymentMethod, Amount, PaymentCode, TransactionCode, Status, PaidAt)
              VALUES (@BookingID, 'Cash', 150000.0, @PaymentCode, @TransactionCode, 'Paid', GETDATE())
            `);
        }
      }
    }
  }

  console.log("Mock booking data generation completed successfully!");
  process.exit(0);
}

run().catch((err) => {
  console.error("Failed to generate mock bookings:", err);
  process.exit(1);
});
